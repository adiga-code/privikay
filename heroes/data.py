# Build registry: key → HeroData
HEROES: dict[str, HeroData] = {
    h["key"]: HeroData(
        key=h["key"],
        name=h["name"],
        emoji=h["emoji"],
        category=h["category"],
        phrases=h["phrases"],
    )
    for h in _HEROES_RAW
}

# Ordered list for display
HERO_KEYS_ORDERED: list[str] = [h["key"] for h in _HEROES_RAW]


def get_hero(key: str) -> HeroData:
    """Return hero by key, falling back to capybara if unknown."""
    return HEROES.get(key, HEROES["capybara"])