import random
from dataclasses import dataclass


@dataclass(frozen=True)
class HeroData:
    key: str
    name: str
    emoji: str
    category: str  # chill | funny | cute | energy | smart
    phrases: dict  # dict[str, list[str]]

    def phrase(self, context: str) -> str:
        """Return a random phrase for the given context."""
        options = self.phrases.get(context, [])
        if not options:
            return ""
        return random.choice(options)


